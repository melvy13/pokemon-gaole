public class ColorCode {
    public static final String RED = "\u001B[31m"; // HP
    public static final String GREEN = "\u001B[32m"; // Winning texts
    public static final String YELLOW = "\u001B[33m"; // Player infos
    public static final String BLUE = "\u001B[34m";
    public static final String PURPLE = "\u001B[35m";
    public static final String CYAN = "\u001B[36m"; // Numbers RNG

    public static final String BRIGHT_RED    = "\u001B[91m"; // Losing texts
    public static final String BRIGHT_GREEN  = "\u001B[92m";
    public static final String BRIGHT_YELLOW = "\u001B[93m";
    public static final String BRIGHT_BLUE   = "\u001B[94m"; // Pokemon names
    public static final String BRIGHT_PURPLE = "\u001B[95m"; // Game phase titles
    public static final String BRIGHT_CYAN   = "\u001B[96m";

    public static final String RESET = "\u001B[0m";
}
